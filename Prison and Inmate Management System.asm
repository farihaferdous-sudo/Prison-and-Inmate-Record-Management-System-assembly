LEA DX, MSG_OCC_TITLE
CALL PRINT_STRING

LEA DX, MSG_OCC_A
CALL PRINT_STRING

MOV AL, BLOCK_OCCUPANCY[0]
MOV AH, 0
CALL PRINT_NUM

LEA DX, MSG_SLASH_CAP
CALL PRINT_STRING

MOV AL, BLOCK_CAPACITY[0]
MOV AH, 0
CALL PRINT_NUM

LEA DX, MSG_OCC_B
CALL PRINT_STRING

MOV AL, BLOCK_OCCUPANCY[1]
MOV AH, 0
CALL PRINT_NUM

LEA DX, MSG_SLASH_CAP
CALL PRINT_STRING

MOV AL, BLOCK_CAPACITY[1]
MOV AH, 0
CALL PRINT_NUM

LEA DX, MSG_OCC_C
CALL PRINT_STRING

MOV AL, BLOCK_OCCUPANCY[2]
MOV AH, 0
CALL PRINT_NUM

LEA DX, MSG_SLASH_CAP
CALL PRINT_STRING

MOV AL, BLOCK_CAPACITY[2]
MOV AH, 0
CALL PRINT_NUM

RET

DISPLAY_BLOCK_OCCUPANCY ENDP


; SUPPORT PROCEDURE FOR FEATURE 1, 2, AND 4

NORMALIZE_DURATION PROC NEAR

MOV AX, DURATION_DAY[SI]
MOV DX, 0
MOV BX, 30
DIV BX

CMP DX, 0
JE NORM_DAY_30

MOV DURATION_DAY[SI], DX
ADD DURATION_MONTH[SI], AX
JMP NORM_DAY_DONE

NORM_DAY_30:

CMP AX, 0
JE NORM_DAY_DONE

DEC AX
MOV DURATION_DAY[SI], 30
ADD DURATION_MONTH[SI], AX

NORM_DAY_DONE:

MOV AX, DURATION_MONTH[SI]
MOV DX, 0
MOV BX, 12
DIV BX

CMP DX, 0
JE NORM_MONTH_12

MOV DURATION_MONTH[SI], DX
ADD DURATION_YEAR[SI], AX
JMP NORM_DONE

NORM_MONTH_12:

CMP AX, 0
JE NORM_DONE

DEC AX
MOV DURATION_MONTH[SI], 12
ADD DURATION_YEAR[SI], AX

NORM_DONE:

RET

NORMALIZE_DURATION ENDP

; FEATURE 2 SUPPORT: RELEASE DATE CALCULATION

CALC_RELEASE PROC NEAR

CALL NORMALIZE_DURATION

MOV AX, INTAKE_DAY[SI]
ADD AX, DURATION_DAY[SI]

MOV DX, 0
MOV BX, 30
DIV BX

CMP DX, 0
JE REL_DAY_30

MOV EXTRA_MONTH, AX
MOV RELEASE_DAY[SI], DX
JMP REL_DAY_DONE

REL_DAY_30:

DEC AX
MOV EXTRA_MONTH, AX
MOV RELEASE_DAY[SI], 30

REL_DAY_DONE:

MOV AX, INTAKE_MONTH[SI]
ADD AX, DURATION_MONTH[SI]
ADD AX, EXTRA_MONTH

MOV DX, 0
MOV BX, 12
DIV BX

CMP DX, 0
JE REL_MONTH_12

MOV EXTRA_YEAR, AX
MOV RELEASE_MONTH[SI], DX
JMP REL_MONTH_DONE

REL_MONTH_12:

DEC AX
MOV EXTRA_YEAR, AX
MOV RELEASE_MONTH[SI], 12

REL_MONTH_DONE:

MOV AX, INTAKE_YEAR[SI]
ADD AX, DURATION_YEAR[SI]
ADD AX, EXTRA_YEAR
MOV RELEASE_YEAR[SI], AX

RET

CALC_RELEASE ENDP


; COMMON SUPPORT PROCEDURE
; PRINT_STRING

PRINT_STRING PROC NEAR

MOV AH, 9
INT 21H

RET

PRINT_STRING ENDP

; COMMON SUPPORT PROCEDURE
; READ_NUM

READ_NUM PROC NEAR

MOV BX, 0
MOV NUM_DIGITS, 0
MOV INPUT_ERR, 0

READ_NUM_LOOP:

MOV AH, 1
INT 21H

CMP AL, 0DH
JE READ_NUM_DONE

CMP AL, '0'
JL READ_NUM_BAD

CMP AL, '9'
JG READ_NUM_BAD

SUB AL, 30H
MOV AH, 0

PUSH AX

MOV AX, BX
MOV CX, 10
MUL CX

MOV BX, AX

POP AX
ADD BX, AX

INC NUM_DIGITS

JMP READ_NUM_LOOP

READ_NUM_BAD:

MOV INPUT_ERR, 1
JMP READ_NUM_LOOP

READ_NUM_DONE:

CMP NUM_DIGITS, 0
JNE READ_NUM_OK

MOV INPUT_ERR, 1

READ_NUM_OK:

MOV AX, BX

RET

READ_NUM ENDP

; COMMON SUPPORT PROCEDURE
; PRINT_NUM

PRINT_NUM PROC NEAR

PUSH AX
PUSH BX
PUSH CX
PUSH DX

CMP AX, 0
JNE PRINT_NUM_CONVERT

MOV DL, '0'
MOV AH, 2
INT 21H

JMP PRINT_NUM_DONE

PRINT_NUM_CONVERT:

MOV CX, 0
MOV BX, 10

PRINT_NUM_LOOP:

MOV DX, 0
DIV BX

ADD DL, 30H
PUSH DX

INC CX

CMP AX, 0
JNE PRINT_NUM_LOOP

PRINT_NUM_SHOW:

POP DX
MOV AH, 2
INT 21H

LOOP PRINT_NUM_SHOW

PRINT_NUM_DONE:

POP DX
POP CX
POP BX
POP AX

RET

PRINT_NUM ENDP

; COMMON SUPPORT PROCEDURE
; READ_STRING_PROC

READ_STRING_PROC PROC NEAR

MOV STR_LEN_TEMP, 0

READ_STRING_LOOP:

MOV AH, 1
INT 21H

CMP AL, 0DH
JE READ_STRING_DONE

MOV BL, STR_LEN_TEMP
CMP BL, MAX_TEMP
JGE READ_STRING_LOOP

MOV [DI], AL
INC DI

INC BL
MOV STR_LEN_TEMP, BL

JMP READ_STRING_LOOP

READ_STRING_DONE:

RET

READ_STRING_PROC ENDP

; FEATURE 1: INMATE INTAKE AND PROFILE CREATION

INTAKE_PROFILE PROC NEAR

LEA DX, MSG_TITLE
CALL PRINT_STRING

ASK_COUNT:

LEA DX, MSG_COUNT
CALL PRINT_STRING

CALL READ_NUM

CMP INPUT_ERR, 1
JE INVALID_COUNT

CMP AX, 10
JG INVALID_COUNT

CMP AX, 1
JL INVALID_COUNT

; check if old count + new count is more than 10
MOV BX, AX

MOV AL, COUNT
MOV AH, 0

ADD AX, BX

CMP AX, 10
JG INVALID_COUNT

; REMAINING = number of new inmates to add
MOV AX, BX
MOV REMAINING, AL

JMP COUNT_OK

INVALID_COUNT:

LEA DX, MSG_INVALID
CALL PRINT_STRING
JMP ASK_COUNT

COUNT_OK:

INPUT_RECORD:

CMP REMAINING, 0
JE FINISH_ALL

; SI = COUNT * 2
; This starts saving after the last existing inmate
MOV AL, COUNT
MOV AH, 0
MOV BX, 2
MUL BX
MOV SI, AX

READ_ID_AGAIN:

LEA DX, MSG_ID
CALL PRINT_STRING

CALL READ_NUM

CMP INPUT_ERR, 1
JE BAD_ID_FEATURE1

MOV TEMP_ID, AX

; check duplicate against all existing saved inmates
MOV AL, COUNT
CMP AL, 0
JE ID_NOT_DUPLICATE

MOV DI, 0
MOV CL, COUNT
MOV CH, 0

CHECK_DUPLICATE:

MOV AX, INMATE_IDS[DI]
CMP AX, TEMP_ID
JE DUPLICATE_FOUND

ADD DI, 2

LOOP CHECK_DUPLICATE

JMP ID_NOT_DUPLICATE

BAD_ID_FEATURE1:

LEA DX, MSG_BAD_INPUT
CALL PRINT_STRING
JMP READ_ID_AGAIN

DUPLICATE_FOUND:

LEA DX, MSG_DUP
CALL PRINT_STRING
JMP READ_ID_AGAIN

ID_NOT_DUPLICATE:

MOV AX, TEMP_ID
MOV INMATE_IDS[SI], AX

LEA DX, MSG_NAME
CALL PRINT_STRING

; name offset = COUNT * 20
MOV AL, COUNT
MOV AH, 0
MOV BX, 20
MUL BX
LEA DI, NAMES
ADD DI, AX

MOV MAX_TEMP, 20
CALL READ_STRING_PROC

MOV AL, COUNT
MOV AH, 0
MOV BX, AX
MOV AL, STR_LEN_TEMP
MOV NAMELEN[BX], AL

LEA DX, MSG_AGE
CALL PRINT_STRING

CALL READ_NUM

CMP INPUT_ERR, 1
JE BAD_AGE

MOV AGE[SI], AX
JMP AGE_OK

BAD_AGE:

LEA DX, MSG_INVALID
CALL PRINT_STRING
JMP INPUT_RECORD

AGE_OK:

LEA DX, MSG_CRIME
CALL PRINT_STRING

; crime offset = COUNT * 15
MOV AL, COUNT
MOV AH, 0
MOV BX, 15
MUL BX
LEA DI, CRIMES
ADD DI, AX

MOV MAX_TEMP, 15
CALL READ_STRING_PROC

MOV AL, COUNT
MOV AH, 0
MOV BX, AX
MOV AL, STR_LEN_TEMP
MOV CRIMELEN[BX], AL

LEA DX, MSG_DUR_TITLE
CALL PRINT_STRING

READ_DURATION_DAY:


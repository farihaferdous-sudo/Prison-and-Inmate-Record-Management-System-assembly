MOV BLOCK_OCCUPANCY[DI], AL

MOV AL, BLOCK_OCCUPANCY[SI]
INC AL
MOV BLOCK_OCCUPANCY[SI], AL

MOV BX, FOUND_INDEX
MOV AL, DEST_BLOCK_TEMP
MOV INMATE_BLOCK[BX], AL

LEA DX, MSG_TRANSFER_OK
CALL PRINT_STRING

LEA DX, MSG_OUT_ID
CALL PRINT_STRING

MOV AX, SEARCH_ID
CALL PRINT_NUM

LEA DX, MSG_OLD_BLOCK
CALL PRINT_STRING

MOV AL, OLD_BLOCK_TEMP
CALL PRINT_BLOCK_NAME

LEA DX, MSG_NEW_BLOCK
CALL PRINT_STRING

MOV AL, DEST_BLOCK_TEMP
CALL PRINT_BLOCK_NAME

CALL DISPLAY_BLOCK_OCCUPANCY

ASK_TRANSFER_AGAIN:

LEA DX, MSG_TRANSFER_AGAIN
CALL PRINT_STRING

MOV AH, 1
INT 21H

CMP AL, 'Y'
JE TRANSFER_START

CMP AL, 'y'
JE TRANSFER_START

CMP AL, 'N'
JE TRANSFER_DONE

CMP AL, 'n'
JE TRANSFER_DONE

JMP ASK_TRANSFER_AGAIN

TRANSFER_DONE:

RET

TRANSFER_BAD_ID:

LEA DX, MSG_BAD_INPUT
CALL PRINT_STRING
JMP TRANSFER_START

TRANSFER_ID_NOT_FOUND:

LEA DX, MSG_NOT_FOUND
CALL PRINT_STRING
JMP TRANSFER_START

TRANSFER_NOT_ASSIGNED:

LEA DX, MSG_NOT_ASSIGNED
CALL PRINT_STRING
JMP ASK_TRANSFER_AGAIN

BAD_DEST_BLOCK:

LEA DX, MSG_BAD_BLOCK
CALL PRINT_STRING
JMP ASK_DEST_BLOCK

TRANSFER_SAME_BLOCK:

LEA DX, MSG_SAME_BLOCK
CALL PRINT_STRING
JMP ASK_DEST_BLOCK

TRANSFER_BLOCK_FULL:

LEA DX, MSG_BLOCK_FULL
CALL PRINT_STRING
CALL DISPLAY_BLOCK_OCCUPANCY
JMP ASK_TRANSFER_AGAIN

TRANSFER_CELL_BLOCK ENDP

; SUPPORT PROCEDURE FOR FEATURE 3 AND FEATURE 6
; PRINT_BLOCK_NAME

PRINT_BLOCK_NAME PROC NEAR

CMP AL, 1
JE PRINT_BLOCK_A

CMP AL, 2
JE PRINT_BLOCK_B

CMP AL, 3
JE PRINT_BLOCK_C

RET

PRINT_BLOCK_A:

LEA DX, MSG_BLOCK_A
CALL PRINT_STRING
RET

PRINT_BLOCK_B:

LEA DX, MSG_BLOCK_B
CALL PRINT_STRING
RET

PRINT_BLOCK_C:

LEA DX, MSG_BLOCK_C
CALL PRINT_STRING
RET

PRINT_BLOCK_NAME ENDP

